/* ------------------- language switching ------------------- */

const GGEN_LANGS = ["me", "en", "it", "de", "zh"];
const GGEN_LANG_ATTR = { me: "cnr", en: "en", it: "it", de: "de", zh: "zh-Hans" };

function ggenGetLang() {
  const saved = localStorage.getItem("ggen-lang");
  return GGEN_LANGS.includes(saved) ? saved : "me";
}

function ggenApplyLang(lang) {
  const dict = I18N[lang] || I18N.me;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const v = dict[el.dataset.i18n];
    if (v != null) el.innerHTML = v;
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    const v = dict[el.dataset.i18nPh];
    if (v != null) el.setAttribute("placeholder", v);
  });
  document.documentElement.lang = GGEN_LANG_ATTR[lang] || "cnr";
  localStorage.setItem("ggen-lang", lang);
  document.querySelectorAll(".lang-toggle button").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });
  // let other scripts (e.g. number formatting) react
  document.dispatchEvent(new CustomEvent("ggen:lang", { detail: { lang } }));
}

document.addEventListener("DOMContentLoaded", () => {
  document.querySelectorAll(".lang-toggle button").forEach((btn) => {
    btn.addEventListener("click", () => ggenApplyLang(btn.dataset.lang));
  });
  ggenApplyLang(ggenGetLang());
});
